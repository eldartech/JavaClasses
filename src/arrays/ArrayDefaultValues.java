package arrays;

public class ArrayDefaultValues {
    public static void main(String[] args) {
        int [] nums = new int[4];
        nums[0]= 5;

        System.out.println(nums[0]);
        System.out.println(nums[1]);
//        Array has an indexing.
//        Array is fixed size
//        Array elements take default values if we
//        don't assign them.
//        int -> 0
//        short -> 0
//        byte ->0
//        float ->0.0
//        double -> 0.0
//        long -> 0
//        boolean -> false
//        char -> ''
//        Objects -> null
    }
}
