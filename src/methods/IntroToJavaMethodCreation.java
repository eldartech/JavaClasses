package methods;


public class IntroToJavaMethodCreation {
    public static void main(String[] args) {

        System.out.println(sumOfNumber(10,50));
        System.out.println(sumOfNumber(4.3,23.4));



    }
    public static int sumOfNumber(int a,int b){

         return a+b;
     }
    public static double sumOfNumber(double a,double b){

        return a+b;
    }
}
